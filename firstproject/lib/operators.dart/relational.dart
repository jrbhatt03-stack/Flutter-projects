void main () {
  int a = 10; // variable declared
  int b = 20; // variable declared

  print(a > b); // if a is greater than b then true else false
  print(a == b); // if a is equal to b then true else false (== means equal to)
  print(a < b); // if a is less than b then true else false
  print(a >= b); // if a is greater than or equal to b then true else false
  print(a <= b); // if a is less than or equal to b then true else false
}